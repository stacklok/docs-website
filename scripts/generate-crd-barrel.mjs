/*
 * Generate the consolidated CRD schema barrel consumed by the <CRDFields>
 * React component (src/components/CRDReference/all-schemas.ts).
 *
 * Reads every CRD set declared in .github/upstream-projects.yaml (via
 * scripts/lib/crd-sets.mjs) and the per-set index.json each set's extract
 * step produced, then emits one TypeScript module that imports every CRD's
 * JSON Schema and maps it by Kind. There is no hand-maintained merge step:
 * add a CRD set to the YAML, regenerate, and the barrel picks it up.
 *
 * CRD Kinds must be globally unique across sets, because both <CRDFields>
 * and the <CRDReference> remark plugin resolve schemas by Kind name. This
 * script throws on a collision rather than letting one set silently shadow
 * another.
 *
 * Usage: node scripts/generate-crd-barrel.mjs
 * (Also run automatically at the end of extract-crds.mjs.)
 */

import fs from 'node:fs';
import path from 'node:path';
import { getCrdSets, repoRoot } from './lib/crd-sets.mjs';

const barrelPath = path.resolve(
  repoRoot,
  'src',
  'components',
  'CRDReference',
  'all-schemas.ts'
);

// Collect { kind, plural, importBase } across all sets, failing on any Kind
// that appears in more than one set.
//
// A declared set whose index.json is absent is skipped with a warning, not
// an error: regenerating one set in isolation (or first-time setup of a new
// set) is a legitimate state, and the other sets' generated files are
// committed build inputs that are normally present. This matches the
// crd-reference-remark plugin, which also skips missing schema dirs. The
// loud failure for a genuinely-missing set happens later, at Docusaurus
// build time, when a page references a Kind that isn't in the barrel.
const byKind = new Map();
const skipped = [];
for (const set of getCrdSets()) {
  const indexPath = path.resolve(repoRoot, set.out, 'index.json');
  if (!fs.existsSync(indexPath)) {
    skipped.push(set.key);
    continue;
  }
  const importBase = '@site/' + set.out.replace(/\\/g, '/');
  const entries = JSON.parse(fs.readFileSync(indexPath, 'utf8'));
  for (const entry of entries) {
    const existing = byKind.get(entry.kind);
    if (existing) {
      throw new Error(
        `Duplicate CRD Kind "${entry.kind}" in sets "${existing.key}" and ` +
          `"${set.key}". CRD Kinds must be globally unique across all sets ` +
          `because <CRDFields> and <CRDReference> resolve schemas by Kind name.`
      );
    }
    byKind.set(entry.kind, {
      key: set.key,
      plural: entry.plural,
      importPath: `${importBase}/${entry.plural}.schema.json`,
    });
  }
}

// Stable alphabetical order by Kind so the generated file diffs cleanly.
const kinds = [...byKind.keys()].sort();

const imports = kinds
  .map((kind) => `import ${kind} from '${byKind.get(kind).importPath}';`)
  .join('\n');

const members = kinds.map((kind) => `  ${kind},`).join('\n');

const content = `// SPDX-FileCopyrightText: Copyright 2026 Stacklok, Inc.
// SPDX-License-Identifier: Apache-2.0

// AUTO-GENERATED by scripts/generate-crd-barrel.mjs. Do not edit.
// Consolidated map of every CRD Kind to its extracted JSON Schema, across all
// CRD sets declared in .github/upstream-projects.yaml. Consumed by the
// <CRDFields> React component; the <CRDReference> remark plugin reads the same
// schemas from disk at build time. Regenerate with extract-crds.mjs or
// \`node scripts/generate-crd-barrel.mjs\`.

${imports}

export const allSchemas = {
${members}
} as const;

export type AnyCRDKind = keyof typeof allSchemas;
`;

fs.writeFileSync(barrelPath, content);
const setCount = getCrdSets().length - skipped.length;
console.log(
  `Wrote ${path.relative(repoRoot, barrelPath)} (${kinds.length} CRD Kind(s) across ${setCount} set(s)).`
);
if (skipped.length) {
  console.warn(
    `Note: skipped ${skipped.length} declared set(s) with no generated index.json: ${skipped.join(', ')}. ` +
      `Run extract-crds.mjs for those projects to include their CRDs in the barrel.`
  );
}
